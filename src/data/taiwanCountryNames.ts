export function getISOByCountryName(name: string): { cca3: string; cca2: string } | null {
  if (!name) return null;
  const n = name.trim().toLowerCase();

  // Checks for Taiwan
  if (
    n === 'taiwan' ||
    n.includes('province of china') ||
    n.includes('provice of china') || // common typo in datasets
    n.includes('republic of china') ||
    n === 'chinese taipei' ||
    n === 'formosa' ||
    n === '台灣' ||
    n === '臺灣' ||
    n === '中華民國'
  ) {
    return { cca3: 'TWN', cca2: 'TW' };
  }

  // Checks for China
  if (
    n === 'china' ||
    n === "people's republic of china" ||
    n === 'peoples republic of china' ||
    n === '中國' ||
    n === '中華人民共和國'
  ) {
    return { cca3: 'CHN', cca2: 'CN' };
  }

  // Checks for Hong Kong
  if (
    n === 'hong kong' ||
    n.includes('hong kong sar') ||
    n.includes('hong kong special administrative region') ||
    n === '香港'
  ) {
    return { cca3: 'HKG', cca2: 'HK' };
  }

  // Checks for Macau/Macao
  if (
    n === 'macau' ||
    n === 'macao' ||
    n.includes('macao sar') ||
    n.includes('macau sar') ||
    n.includes('macao special administrative region') ||
    n.includes('macau special administrative region') ||
    n === '澳門'
  ) {
    return { cca3: 'MAC', cca2: 'MO' };
  }

  // Checks for South Korea
  if (
    n === 'south korea' ||
    n === 'korea, republic of' ||
    n === 'republic of korea' ||
    n === 'korea, south' ||
    n === '韓國' ||
    n === '大韓民國'
  ) {
    return { cca3: 'KOR', cca2: 'KR' };
  }

  // Checks for North Korea
  if (
    n === 'north korea' ||
    n === "korea, democratic people's republic of" ||
    n === 'democratic peoples republic of korea' ||
    n === 'korea, north' ||
    n === '北韓' ||
    n === '朝鮮'
  ) {
    return { cca3: 'PRK', cca2: 'KP' };
  }

  // Checks for USA
  if (
    n === 'united states' ||
    n === 'united states of america' ||
    n === 'usa' ||
    n === 'us' ||
    n === '美國'
  ) {
    return { cca3: 'USA', cca2: 'US' };
  }

  // Checks for UK
  if (
    n === 'united kingdom' ||
    n === 'united kingdom of great britain and northern ireland' ||
    n === 'uk' ||
    n === 'great britain' ||
    n === '英國'
  ) {
    return { cca3: 'GBR', cca2: 'GB' };
  }

  // Vietnam
  if (n === 'vietnam' || n === 'viet nam' || n === '越南') {
    return { cca3: 'VNM', cca2: 'VN' };
  }

  // Russia
  if (n === 'russia' || n === 'russian federation' || n === '俄羅斯') {
    return { cca3: 'RUS', cca2: 'RU' };
  }

  return null;
}

export function getLocalizedCountryDetails(
  cca3: string,
  originalCapital?: string,
  originalCurrencyName?: string,
  originalCurrencyKeys?: string[]
): {
  capital: string;
  currencyName: string;
  currencyCode: string;
} {
  const code = (cca3 || '').trim().toUpperCase();

  // 1. Check if we have standard direct details mapped for this country
  if (TAIWAN_COUNTRY_DETAILS[code]) {
    return { ...TAIWAN_COUNTRY_DETAILS[code] };
  }

  // 2. Perform fallback mapping
  let mappedCapital = '無';
  if (originalCapital && originalCapital !== '無' && originalCapital !== 'None') {
    const key = originalCapital.trim();
    mappedCapital = TAIWAN_CAPITALS[key] || CAPITAL_TRANSLATIONS[key] || translateCountryToTraditional(code, key);
  }

  let mappedCurrencyName = '未知';
  if (originalCurrencyName && originalCurrencyName !== '未知') {
    const key = originalCurrencyName.trim();
    mappedCurrencyName = CURRENCY_NAME_TRANSLATIONS[key] || translateCountryToTraditional(code, key);
  } else if (originalCurrencyKeys && originalCurrencyKeys.length > 0) {
    const symbolCode = originalCurrencyKeys[0].trim().toUpperCase();
    if (CURRENCY_NAME_TRANSLATIONS[symbolCode]) {
      mappedCurrencyName = CURRENCY_NAME_TRANSLATIONS[symbolCode];
    }
  }

  let mappedCurrencyCode = '未知代碼';
  if (originalCurrencyKeys && originalCurrencyKeys.length > 0) {
    const symbolCode = originalCurrencyKeys[0].trim().toUpperCase();
    // Use the translated name if feasible
    mappedCurrencyCode = `${mappedCurrencyName}代代號 / 縮寫 (${symbolCode})`;
  } else {
    mappedCurrencyCode = `${mappedCurrencyName}代號`;
  }

  return {
    capital: mappedCapital,
    currencyName: mappedCurrencyName,
    currencyCode: mappedCurrencyCode
  };
}

